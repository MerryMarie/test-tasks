<?php
namespace CV;

class Classificator{

    /**
     * Определение фигуры 
     * 
     * @param array $matrix
     * 
     * @return string 
     */
    static public function identifyFigure(array $matrix):string
    {
        $altMatrix = array();

        foreach($matrix as $k => $v){
            
            for($i = 0; $i < count($v) - 1; $i++){
                if(
                    $v[$i] == 0 
                    && $v[$i + 1] == 1
                ){
                      $altMatrix[$k][0] = [$i + 2, $k + 1];
                }
                if(
                    $v[$i] == 1 
                    && $v[$i + 1] == 0
                ){
                     $altMatrix[$k][1] = [$i + 1,$k + 1];
                }
                $altMatrix[$k][2] = array_sum($v);
            }
        }

        $resultArray = array_filter($altMatrix, function ($var) {
            return($var[2] != 0);
        });
        $r = $resultArray;
        $topRow = array_shift($r);
        $bottomRow = array_pop($r);

        usort($resultArray,function($a,$b){
            if ($a[2] == $b[2]) return 0;
            return ($a[2] < $b[2]) ? 1 : -1;
        });
        $maxRow =  array_shift($resultArray);
        
        if(
            $topRow[0][0] == $bottomRow[0][0] 
            && $topRow[1][0] == $bottomRow[1][0]
            && $maxRow[0][0] < $topRow[0][0]
            && $maxRow[1][0] > $topRow[1][0]
        ){
                if($topRow[0][0] == $topRow[1][0]){
                    $output = "square";
                }else{
                    $output = "circle";
                }
        }elseif(
            $maxRow[2] == $topRow[2]
            && $maxRow[2] == $bottomRow[2] 
            ||($maxRow[0][0] < $topRow[0][0]
            && $maxRow[1][0] > $bottomRow[1][0] 
            || $maxRow[0][0] < $bottomRow[0][0] 
            && $maxRow[1][0] < $topRow[1][0])
        ){
           $output = "square";
        }else{
            $output = "triangle";
        }

        return $output;
        
    }

    /**
     * Считывает данные из консоли
     * 
     * @return array
     */
    static public function readData():array
    {

        $XY = explode(" ", readline());
        
        $matrix = array();
        for($i = 0; $i < (int)$XY[1]; $i++){
            $matrix[] = explode(" ", readline());
        }
        return $matrix;
    }


     /**
     * Разбивает массив строк
     * 
     * @param array $data
     * 
     * @return array
     */
    static public function prepareData(array $data):array
    {
        $matrix = array();
        foreach($data as $k => $v){
            $matrix[] = explode(" ", $v);
        }
        return $matrix;
    }

    


}

